package edu.training.jc.linnear_program;

/**
 * Составить алгоритм нахождения среднего арифметического двух чисел
 */

public class Linnear3 {

	public static void main(String[] args) {
		
		double x;
		double y;
		double averageValue;

		x = 5.2;
		y = 7;
		
		averageValue = (x+y)/2;
		
		System.out.println("Среднее значение чисел " + x + " и " + y + " равно " + averageValue);
	}
}
